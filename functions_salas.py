# Functions
def salas_function():
  print("Joe Salas")
  print("Jackie Salas")
  print("Julian Salas")
  print("Jasper Salas")

salas_function()

def salas_function_arg(arg1, arg2, arg3, arg4):
  print(f"arg1: {arg1}, arg2: {arg2}, arg3: {arg3}, arg4: {arg4}")

salas_function_arg("One","Two","Three","Four")

def salas_function_arg_clean(arg1, arg2, arg3, arg4):
  print(f"{arg1}, {arg2}, {arg3}, {arg4}")

salas_function_arg_clean("One","Two","Three","Four")
